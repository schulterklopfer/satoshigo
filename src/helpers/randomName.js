export function randomEmojiString(size) {
  let r = [];

  for (let i = 0; i < size; i++) {
    let pick;
    do {
      pick = EMOJIS[Math.round(Math.random() * (EMOJIS.length - 1))];
    } while (r.indexOf(pick) > -1);
    r.push(pick);
  }
  return r.join('');
}

